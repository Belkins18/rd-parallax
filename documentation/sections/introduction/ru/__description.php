<h2 class="item1">Введение</h2>

<h5>
    Данный скрипт предназначен для эмуляции эффекта
    <a href="https://en.wikipedia.org/wiki/Parallax">Параллакса</a>
    на
    страницах шаблона.
</h5>

<h3>С чего начать?</h3>

<ul class="marked-list">
    <li>
        Демонстрацию работы данного скрипта можно посмотреть по следующей ссылке:
        <a href='./index.php?lang=ru&section=demo'>Демо</a>
        .
    </li>
    <li>
        Сам скрипт доступен в нашем публичном репозитории:
        <a href='https://github.com/TemplatemonsterPlugins/rd-parallax'>Git</a>
    </li>
</ul>

